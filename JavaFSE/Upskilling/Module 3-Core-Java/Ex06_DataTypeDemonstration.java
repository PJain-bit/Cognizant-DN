public class Ex06_DataTypeDemonstration {
    public static void main(String[] args) {
        // Task: Declare variables of different primitive types and display their values.
        int integerVar = 100;
        float floatVar = 5.99f;
        double doubleVar = 19.99;
        char charVar = 'A';
        boolean booleanVar = true;
        
        // Displaying each variable
        System.out.println("Integer Value: " + integerVar);
        System.out.println("Float Value: " + floatVar);
        System.out.println("Double Value: " + doubleVar);
        System.out.println("Character Value: " + charVar);
        System.out.println("Boolean Value: " + booleanVar);
    }
}
